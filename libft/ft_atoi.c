#include "libft.h"

int	ft_atoi(const char *str)
{
	int	nbr;
	int	sign;
	int	i;

	nbr = 0;
	sign = 1;
	i = 0;
	while (str[i] == ' ' || str[i] == '\n' || str[i] == '\t'
		|| str[i] == '\f' || str[i] == '\v' || str[i] == '\r')
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
		nbr = -nbr;
	}
	i--;
	while (ft_isdigit (str[++i]))
		if (nbr == 0)
			nbr += str[i] - '0';
	else
			nbr = (nbr * 10) + str[i] - '0';
	return (nbr * sign);
}
